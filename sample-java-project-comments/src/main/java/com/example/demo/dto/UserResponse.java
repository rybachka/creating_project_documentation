package com.example.demo.dto;

/** Odpowiedź użytkownika. */
public class UserResponse {
    /** Unikalny identyfikator użytkownika (UUID). */
    public String id;
    /** Nazwa wyświetlana lub imię i nazwisko. */
    public String name;
    /** Adres e-mail w formacie RFC 5322. */
    public String email;
}
